"""
Константы для работы с устройством ЭМИС ModBus СКВТ.

Версия: 1.08 
Назначение: Централизованное хранение всех «магических чисел» и настроек.

Примечание:
    Все значения взяты из технической документации на устройство.
    Изменение этих констант без обновления логики в device.py приведёт к ошибкам!
"""

# =============================================================================
# 1. Адреса регистров ModBus (в формате HEX)
# =============================================================================

# Регистры используются для чтения/записи параметров и времени.

# Регистр разрешения программирования (запись 0x5AA5 разрешает изменение настроек)
REG_EN_PROG = 0xA000

# Регистр локального адреса устройства (ModBus Slave ID)
REG_LOCAL_ADDR = 0xA001

# Регистр кода скорости (сохраняется не сама скорость, а её код: 0=1200, 1=2400 и т.д.)
REG_BAUD_CODE = 0xA002

# Регистр и маппинг четности (программная настройка)
REG_PARITY = 0xA003  # U16: 0=None, 1=Odd, 2=Even
PARITY_VAL_TO_STR = {0: "None", 1: "Odd", 2: "Even"}
PARITY_STR_TO_VAL = {"None": 0, "Odd": 1, "Even": 2}

# Начальный адрес блока регистров для хранения даты и времени (3 регистра подряд, big-endian)
# Структура:
#   0xB001: [YY][MM]  → год (2 последние цифры), месяц
#   0xB002: [DD][HH]  → день, час
#   0xB003: [mm][SS]  → минуты, секунды
REG_TIME_START = 0xB001  # Занимает регистры 0xB001, 0xB002, 0xB003


# =============================================================================
# 2. Параметры последовательного порта
# =============================================================================

# Поддерживаемые скорости передачи (бит/с)
BAUD_VALUES = [1200, 2400, 4800, 9600, 19200]

# Соответствие скорости → коду для записи в REG_BAUD_CODE
BAUD_CODE_FROM_VAL = {
    1200: 0,
    2400: 1,
    4800: 2,
    9600: 3,
    19200: 4,
}

# Обратное соответствие: код → скорость (для чтения из REG_BAUD_CODE)
BAUD_VAL_FROM_CODE = {v: k for k, v in BAUD_CODE_FROM_VAL.items()}

# Допустимые значения чётности для GUI (отображаются пользователю)
PARITY_LIST = ["Even", "Odd", "None"]

# Соответствие чётности → символу, понятному minimalmodbus
# minimalmodbus использует однобуквенные обозначения:
#   "E" = Even, "O" = Odd, "N" = None
PARITY_MAP = {
    "Even": "E",
    "Odd": "O",
    "None": "N",
}


# =============================================================================
# 3. Регистры электроэнергии (только чтение)
# =============================================================================

# Показания параметров одноканального счетчика (чтение)
REG_VOLTAGE = 0x016E   # Напряжение (2 регистра, I32)
REG_CURRENT = 0x0174   # Ток (2 регистра, I32)
REG_POWER   = 0x017A   # Мощность (2 регистра, I32)

# Показания параметров первого канала двухканального счетчика (чтение)
REG_VOLTAGE_CH1 = 0x203C  # Напряжение (2 регистра, I32)
REG_CURRENT_CH1 = 0x2040  # Ток (2 регистра, I32)
REG_POWER_CH1   = 0x2044  # Мощность (2 регистра, I32)

# Показания параметров второго канала двухканального счетчика (чтение)
REG_VOLTAGE_CH2 = 0x203E  # Напряжение (2 регистра, I32)
REG_CURRENT_CH2 = 0x2042  # Ток (2 регистра, I32)
REG_POWER_CH2   = 0x2046  # Мощность (2 регистра, I32)

# Коэффициенты для перевода в физические величины
VOLTAGE_FACTOR = 0.0001  # коэффициент перевода напряжения (В)
CURRENT_FACTOR = 0.0001  # коэффициент перевода тока (А)
POWER_FACTOR   = 0.0001  # коэффициент перевода мощности (кВт)

# Коэффициент для перевода энергии(кВт·ч) 
ENERGY_FACTOR = 0.01

# Регистр очистки всех данных энергии
REG_ENERGY_CLEAR = 0xB000
